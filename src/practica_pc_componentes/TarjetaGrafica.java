package practica_pc_componentes;

public class TarjetaGrafica {
    String marca;
    String tipoGrafica;
    String tamanyoMemoria;
    String tipoMemoria;
    double precio;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getTipoGrafica() {
        return tipoGrafica;
    }

    public void setTipoGrafica(String tipoGrafica) {
        this.tipoGrafica = tipoGrafica;
    }

    public String getTamanyoMemoria() {
        return tamanyoMemoria;
    }

    public void setTamanyoMemoria(String tamanyoMemoria) {
        this.tamanyoMemoria = tamanyoMemoria;
    }

    public String getTipoMemoria() {
        return tipoMemoria;
    }

    public void setTipoMemoria(String tipoMemoria) {
        this.tipoMemoria = tipoMemoria;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
}
